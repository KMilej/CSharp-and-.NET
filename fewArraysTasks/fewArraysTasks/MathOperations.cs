using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace fewArraysTasks
{
    internal class MathOperations
    {
        // Method 1: Adds 10 to the input number
        public int AddTen(int number)
        {
            return number + 10;  // Adds 10 to the received parameter and returns the result
        }

        // Method 2: Multiplies the input number by 5
        public int MultiplyByFive(int number)
        {
            return number * 5;  // Multiplies the received parameter by 5 and returns the result
        }

        // Method 3: Subtracts 3 from the input number
        public int SubtractThree(int number)
        {
            return number - 3;  // Subtracts 3 from the received parameter and returns the result
        }
    }
}
